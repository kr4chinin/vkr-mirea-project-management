'use client';

import { useUser } from '@clerk/nextjs';
import { CheckCircleIcon } from '@heroicons/react/24/outline';
import { zodResolver } from '@hookform/resolvers/zod';
import { TaskStatus, type Task } from '@prisma/client';
import { redirect, useRouter } from 'next/navigation';
import { useState, type ReactNode } from 'react';
import { useForm } from 'react-hook-form';
import { toast } from 'sonner';
import * as z from 'zod';
import { Button } from '~/components/ui/button';
import { DatePicker } from '~/components/ui/date-picker';
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
  DialogTrigger,
} from '~/components/ui/dialog';
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from '~/components/ui/form';
import { Input } from '~/components/ui/input';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '~/components/ui/select';
import { Textarea } from '~/components/ui/textarea';
import { AppRoutes, RoutePath } from '~/config/routeConfig';
import { cn, getReadableTaskStatusLabel } from '~/lib/utils';
import { api } from '~/trpc/react';

const formSchema = z.object({
  name: z.string().min(1, { message: '–ù–µ–æ–±—Ö–æ–¥–∏–º–æ —É–∫–∞–∑–∞—Ç—å –Ω–∞–∑–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏' }),
  description: z.string(),
  startDate: z.date().optional(),
  endDate: z.date().optional(),
  status: z.nativeEnum(TaskStatus).optional(),
});

interface Props {
  projectId: number;
  button: ReactNode;
  task?: Task;
}

export function ProjectTaskDialog(props: Props) {
  const { projectId, task, button } = props;

  const router = useRouter();

  const { user } = useUser();

  const createTask = api.task.createTask.useMutation({
    onSuccess: () => {
      router.refresh();

      toast.success('–ó–∞–¥–∞—á–∞ —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω–∞', { icon: 'üíé' });
    },
  });

  const changeTaskIsCompleteState = api.task.changeTaskIsCompletedState.useMutation({
    onSuccess: () => {
      router.refresh();

      toast.success(
        task?.isCompleted ? '–ó–∞–¥–∞—á–∞ —É—Å–ø–µ—à–Ω–æ —Å–¥–µ–ª–∞–Ω–∞ –∞–∫—Ç–∏–≤–Ω–æ–π' : '–ó–∞–¥–∞—á–∞ —É—Å–ø–µ—à–Ω–æ –≤—ã–ø–æ–ª–Ω–µ–Ω–∞',
        {
          icon: task?.isCompleted ? '‚úÖ' : 'üéâ',
        }
      );
    },
  });

  const updateTask = api.task.updateTask.useMutation({
    onSuccess: () => {
      router.refresh();

      toast.success('–ó–∞–¥–∞—á–∞ —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∞', { icon: '‚úèÔ∏è' });
    },
  });

  const [opened, setOpened] = useState(false);

  const form = useForm<z.infer<typeof formSchema>>({
    resolver: zodResolver(formSchema),
    defaultValues: {
      name: task?.name ?? '',
      description: task?.description ?? '',
      startDate: task?.startDate ?? undefined,
      endDate: task?.endDate ?? undefined,
      status: task?.status,
    },
  });

  const handleSubmit = async (values: z.infer<typeof formSchema>) => {
    if (task) {
      await updateTask.mutateAsync({
        id: task.id,
        name: values.name,
        description: values.description,
        startDate: values.startDate,
        endDate: values.endDate,
        status: values.status,
      });
    } else {
      if (!user) redirect(RoutePath[AppRoutes.SIGN_IN]);

      await createTask.mutateAsync({
        projectId,
        name: values.name,
        createdBy: user.id,
        description: values.description,
        startDate: values.startDate,
        endDate: values.endDate,
        status: values.status,
      });

      form.reset();
    }
  };

  const handleChangeTaskIsCompleteState = async () => {
    if (!task) throw new Error('Task does not exist yet, failed to change isComplete state');

    await handleSubmit(form.getValues());
    await changeTaskIsCompleteState.mutateAsync({ id: task.id, isCompleted: !task.isCompleted });

    setOpened(false);
  };

  return (
    <Dialog open={opened} onOpenChange={setOpened}>
      <DialogTrigger asChild>{button}</DialogTrigger>

      <DialogContent className="max-w-[540px]">
        <DialogHeader>
          <DialogTitle>{task ? '–í–Ω–µ—Å–∏—Ç–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ –∑–∞–¥–∞—á—É' : '–°–æ–∑–¥–∞–π—Ç–µ –∑–∞–¥–∞—á—É'}</DialogTitle>
          <DialogDescription>
            {task
              ? '–í—ã –Ω–∞—Ö–æ–¥–∏—Ç–µ—Å—å –≤ —Ä–µ–∂–∏–º–µ —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –∑–∞–¥–∞—á–∏'
              : '–°–æ–∑–¥–∞–π—Ç–µ –∑–∞–¥–∞—á—É –≤ –≤–∞—à–µ–º –ø—Ä–æ–µ–∫—Ç–µ'}
          </DialogDescription>
        </DialogHeader>

        <Form {...form}>
          <form
            className="flex flex-col gap-4"
            onSubmit={async e => {
              await form.handleSubmit(handleSubmit)(e);

              setOpened(false);
            }}
          >
            <FormField
              name="name"
              control={form.control}
              render={({ field }) => (
                <FormItem className="flex flex-col gap-1">
                  <FormLabel>–ù–∞–∑–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏</FormLabel>
                  <FormControl>
                    <Input placeholder="–í–≤–µ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏" {...field} />
                  </FormControl>

                  <FormMessage />
                </FormItem>
              )}
            />

            <div className="flex gap-4">
              <FormField
                name="startDate"
                control={form.control}
                render={({ field }) => (
                  <FormItem className="flex flex-col gap-1">
                    <FormLabel>–ù–∞—á–∞–ª–æ –∑–∞–¥–∞—á–∏</FormLabel>
                    <FormControl>
                      <DatePicker selected={field.value} onSelect={field.onChange} />
                    </FormControl>

                    <FormMessage />
                  </FormItem>
                )}
              />

              <FormField
                name="endDate"
                control={form.control}
                render={({ field }) => (
                  <FormItem className="flex flex-col gap-1">
                    <FormLabel>–û–∫–æ–Ω—á–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏</FormLabel>
                    <FormControl>
                      <DatePicker selected={field.value} onSelect={field.onChange} />
                    </FormControl>

                    <FormMessage />
                  </FormItem>
                )}
              />
            </div>

            <FormField
              name="status"
              control={form.control}
              render={({ field }) => (
                <FormItem className="flex flex-col gap-1">
                  <FormLabel>–°—Ç–∞—Ç—É—Å –∑–∞–¥–∞—á–∏</FormLabel>

                  <FormControl>
                    <Select value={field.value} onValueChange={field.onChange}>
                      <SelectTrigger className="w-[240px]">
                        <SelectValue placeholder="–í—ã–±–µ—Ä–∏—Ç–µ —Å—Ç–∞—Ç—É—Å" />
                      </SelectTrigger>
                      <SelectContent>
                        {Object.values(TaskStatus).map(status => (
                          <SelectItem key={status} value={status}>
                            {getReadableTaskStatusLabel(status)}
                          </SelectItem>
                        ))}
                      </SelectContent>
                    </Select>
                  </FormControl>
                </FormItem>
              )}
            />

            <FormField
              name="description"
              control={form.control}
              render={({ field }) => (
                <FormItem className="flex flex-col gap-1">
                  <FormLabel>–û–ø–∏—Å–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏</FormLabel>
                  <FormControl>
                    <Textarea rows={6} placeholder="–í–≤–µ–¥–∏—Ç–µ –æ–ø–∏—Å–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏" {...field} />
                  </FormControl>

                  <FormMessage />
                </FormItem>
              )}
            />

            <div className="flex items-center gap-2">
              {task && (
                <Button
                  type="button"
                  variant={task.isCompleted ? 'outline' : 'default'}
                  className={cn({
                    'flex flex-1 items-center gap-2 bg-green-600 hover:bg-green-700 active:bg-green-600':
                      !task.isCompleted,
                  })}
                  onClick={handleChangeTaskIsCompleteState}
                >
                  {!task.isCompleted && (
                    <div className="flex h-[20px] w-[20px] shrink-0 items-center justify-center">
                      <CheckCircleIcon stroke="white" />
                    </div>
                  )}

                  {task.isCompleted ? '–°–¥–µ–ª–∞—Ç—å –∞–∫—Ç–∏–≤–Ω–æ–π' : '–ó–∞–≤–µ—Ä—à–∏—Ç—å –∑–∞–¥–∞—á—É'}
                </Button>
              )}

              <Button className="flex-1" type="submit" disabled={createTask.isPending}>
                {task ? '–°–æ—Ö—Ä–∞–Ω–∏—Ç—å –∑–∞–¥–∞—á—É' : '–°–æ–∑–¥–∞—Ç—å –∑–∞–¥–∞—á—É'}
              </Button>
            </div>
          </form>
        </Form>
      </DialogContent>
    </Dialog>
  );
}
